public class variables {
    public static void main(String[] args) {
        // FINAL KEYWORD MEANS VARIABLE IS A CONSTANT

        final String name = "John";
        int age = 27;
        float height = 1.82f;
        boolean isMale = true;
        char initial = 'J';

        System.out.println(name + ' ' + age + ' ' + height + ' ' + isMale + ' ' + initial);

    }
}
